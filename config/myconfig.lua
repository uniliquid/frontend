Liquid ist ausschließlich zur Nutzung für die Teilhabe an der Meinungs- und Willensbildung innerhalb der Piratenpartei gedacht. Eine Nutzung der Plattform für gewerbliche, geschäftliche oder andere Zwecke jenseits der Bestimmung von Liquid ist nicht gestattet. Dieses Verbot gilt in der gleichen Weise im Verhältnis der Teilnehmer untereinander, sofern die betreffende Kommunikation unter Nutzung von Liquid und seiner Funktionen erfolgt. \
